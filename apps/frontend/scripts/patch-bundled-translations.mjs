// ─────────────────────────────────────────────────────────────────────────────
// Deep merge с приоритетом patch.
// ─────────────────────────────────────────────────────────────────────────────
function deepMerge(target, patch) {
  for (const [key, val] of Object.entries(patch)) {
    if (val && typeof val === 'object' && !Array.isArray(val)) {
      if (typeof target[key] !== 'object' || target[key] === null || Array.isArray(target[key])) {
        target[key] = {};
      }
      deepMerge(target[key], val);
    } else {
      target[key] = val;
    }
  }
  return target;
}

async function main() {
  let count = 0;
  for (const [lang, patch] of Object.entries(PATCHES)) {
    const file = path.join(LOCALES_DIR, lang, 'common.json');
    const raw = await fs.readFile(file, 'utf-8');
    const current = JSON.parse(raw);
    deepMerge(current, patch);
    // Снимаем флаг needsRefresh — мы только что положили качественные ручные переводы.
    if (current._meta) {
      delete current._meta.needsRefresh;
      if (Object.keys(current._meta).length === 0) delete current._meta;
    }
    await fs.writeFile(file, JSON.stringify(current, null, 2) + '\n', 'utf-8');
    console.log(`✅ ${lang}: применён ручной патч (${Object.keys(patch).length} top-level секций)`);
    count++;
  }
  console.log(`\n🎉 Готово: обновлено ${count} bundled языков.`);
}

main().catch((err) => {
  console.error('❌ Ошибка:', err);
  process.exit(1);
});
